import UIKit

extension UIColor {
    
    class func paPurple(_ alpha: CGFloat = 1.0) -> UIColor {
        return UIColor.init(red: 70.0/255.0, green: 57.0/255.0, blue: 120.0/255.0, alpha: alpha)
    }
    
}
